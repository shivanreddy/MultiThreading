// C++ program to take the input of
// two numbers and prints its sum
#include <bits/stdc++.h>
using namespace std;

// Driver Code
int main()
{
    int a, b;

    // Input two numbers
    cin >> a >> b;

    // Find the sum
    int sum = a + b;

    // Print the sum
    cout << sum;

    return 0;
}

